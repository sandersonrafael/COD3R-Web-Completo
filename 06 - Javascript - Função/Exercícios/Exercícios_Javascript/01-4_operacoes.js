function Calcular (valor1, valor2) {
    console.log(`A soma dos valores é: ${valor1+valor2}`)
    console.log(`A subtração dos valores é: ${valor1-valor2}`)
    console.log(`A multiplicação dos valores é: ${valor1*valor2}`)
    console.log(`A divisão dos valores é: ${valor1/valor2}`)
}

